// Build reverse index: maps translated text -> English key
// Enables bidirectional translation (e.g. French -> Spanish)
export const REVERSE_INDEX = {};
for (const [englishPhrase, translations] of Object.entries(DEMO_TRANSLATIONS)) {
  for (const [lang, text] of Object.entries(translations)) {
    if (lang !== 'English') {
      REVERSE_INDEX[text.toLowerCase().trim()] = englishPhrase;
    }
  }
}
